            $code .= "    ]);\n";
            break;
            
        case 'markdown':
            $src = isset($data['content']) ? $data['content'] : 'documentation/README.md';
            $code .= "    echo block_markdown('" . addslashes($src) . "');\n";
            break;
            
        case 'codeblock':
            $type = isset($data['type']) ? $data['type'] : 'html';
            $content = isset($data['content']) ? $data['content'] : '';
            $triggerObject = isset($data['trigger_object']) ? $data['trigger_object'] : '';
            $alertObject = isset($data['alert_object']) ? $data['alert_object'] : '';
            $codeblockId = isset($data['codeblock_id']) ? $data['codeblock_id'] : '';
            $class = isset($data['class']) ? $data['class'] : '';
            
            // Use heredoc for content to preserve formatting
            $contentVar = 'CODEBLOCK_' . uniqid();
            $code .= "    \${$contentVar} = <<<'EOD'\n";
            $code .= $content . "\n";
            $code .= "EOD;\n";
            $code .= "    echo block_codeblock([\n";
            $code .= "        'type' => '" . addslashes($type) . "',\n";
            $code .= "        'content' => \${$contentVar},\n";
            if ($type === 'action') {
                if ($triggerObject) $code .= "        'trigger_object' => '" . addslashes($triggerObject) . "',\n";
