            $code .= "    echo block_anchor([\n";
            if ($anchorId) $code .= "        'id' => '" . addslashes($anchorId) . "',\n";
            if ($class) $code .= "        'class' => '" . addslashes($class) . "',\n";
            $code .= "    ]);\n";
