            $method = isset($data['method']) ? $data['method'] : 'POST';
            $class = isset($data['class']) ? $data['class'] : '';
            
            // Use output buffering to capture child blocks
            $code .= "    ob_start();\n";
            
            // Generate child blocks - each generates their own echo (skip PHP tags)
            if (isset($block['children']) && is_array($block['children'])) {
                foreach ($block['children'] as $child) {
                    $code .= generateBlockCode($child, true);
                }
            }
            
            // Capture output and pass to form
            $code .= "    \$formContent = ob_get_clean();\n";
            $code .= "    echo block_form([\n";
            $code .= "        'action' => '" . addslashes($action) . "',\n";
            $code .= "        'method' => '{$method}',\n";
            $code .= "        'class' => '" . addslashes($class) . "',\n";
