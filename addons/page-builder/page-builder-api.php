/**
 * Build inline style string from block data
 */
function buildInlineStyles($data) {
    $styles = [];
    
    // Display Type
    if (!empty($data['display_type']) && $data['display_type'] !== 'block') {
        $styles[] = 'display: ' . $data['display_type'];
    }
    
    // Flow Direction (for flex containers)
    $flowDirection = !empty($data['flow_direction']) ? $data['flow_direction'] : 'vertical';
    if (!empty($data['flow_direction'])) {
        if ($data['flow_direction'] === 'horizontal') {
            $styles[] = 'flex-direction: row';
        } elseif ($data['flow_direction'] === 'vertical') {
            $styles[] = 'flex-direction: column';
        }
    }
    
    // Gap (spacing between flex items)
    if (!empty($data['gap'])) {
        $styles[] = 'gap: ' . $data['gap'];
    }
    
    // Horizontal Alignment
    // When flow is horizontal (row): justify-content controls horizontal alignment
    // When flow is vertical (column): align-items controls horizontal alignment
    if (!empty($data['horizontal_align'])) {
        $align = $data['horizontal_align'];
        $property = ($flowDirection === 'horizontal') ? 'justify-content' : 'align-items';
        
        if ($align === 'left') {
            $styles[] = $property . ': flex-start';
        } elseif ($align === 'center') {
            $styles[] = $property . ': center';
        } elseif ($align === 'right') {
            $styles[] = $property . ': flex-end';
        } elseif ($align === 'space-between' && $flowDirection === 'horizontal') {
            $styles[] = 'justify-content: space-between';
        } elseif ($align === 'space-around' && $flowDirection === 'horizontal') {
            $styles[] = 'justify-content: space-around';
        }
    }
    
    // Text Alignment
    if (!empty($data['text_align'])) {
        $styles[] = 'text-align: ' . $data['text_align'];
    }
    
    // Alignment (for buttons)
    if (!empty($data['alignment'])) {
        $styles[] = 'text-align: ' . $data['alignment'];
    }
    
    // Background
    if (!empty($data['background_type'])) {
        if ($data['background_type'] === 'color' && !empty($data['background_color'])) {
            $styles[] = 'background-color: ' . $data['background_color'];
        } elseif ($data['background_type'] === 'gradient' && !empty($data['background_gradient'])) {
            $styles[] = 'background: ' . $data['background_gradient'];
        } elseif ($data['background_type'] === 'image' && !empty($data['background_image'])) {
            $styles[] = 'background-image: url(' . $data['background_image'] . ')';
            $styles[] = 'background-size: cover';
            $styles[] = 'background-position: center';
        }
    }
    
    // Border
    if (!empty($data['border_style']) && $data['border_style'] !== 'none') {
        $borderWidth = !empty($data['border_width']) ? $data['border_width'] : '1px';
        $borderColor = !empty($data['border_color']) ? $data['border_color'] : '#000000';
        $styles[] = 'border: ' . $borderWidth . ' ' . $data['border_style'] . ' ' . $borderColor;
    }
    
    // Corner Radius
    if (!empty($data['corner_radius'])) {
        $styles[] = 'border-radius: ' . $data['corner_radius'];
    }
    
    // Border Radius (for buttons)
    if (!empty($data['border_radius'])) {
        $styles[] = 'border-radius: ' . $data['border_radius'];
    }
    
    // Box Shadow
    if (!empty($data['shadow'])) {
        $styles[] = 'box-shadow: ' . $data['shadow'];
    }
    
    // Font Family
    if (!empty($data['font_family']) && $data['font_family'] !== 'default') {
        $styles[] = 'font-family: ' . $data['font_family'];
    }
    
    // Font Size
    if (!empty($data['font_size'])) {
        $styles[] = 'font-size: ' . $data['font_size'];
    }
    
    // Text Color
    if (!empty($data['text_color'])) {
        $styles[] = 'color: ' . $data['text_color'];
    }
    
    // Padding
    if (isset($data['padding_type'])) {
        if ($data['padding_type'] === 'all' && !empty($data['padding_all'])) {
            $styles[] = 'padding: ' . $data['padding_all'];
        } elseif ($data['padding_type'] === 'individual') {
            if (!empty($data['padding_top'])) $styles[] = 'padding-top: ' . $data['padding_top'];
            if (!empty($data['padding_right'])) $styles[] = 'padding-right: ' . $data['padding_right'];
            if (!empty($data['padding_bottom'])) $styles[] = 'padding-bottom: ' . $data['padding_bottom'];
            if (!empty($data['padding_left'])) $styles[] = 'padding-left: ' . $data['padding_left'];
        }
    }
    
    // Margin
    if (isset($data['margin_type'])) {
        if ($data['margin_type'] === 'all' && !empty($data['margin_all'])) {
            $styles[] = 'margin: ' . $data['margin_all'];
        } elseif ($data['margin_type'] === 'individual') {
            if (!empty($data['margin_top'])) $styles[] = 'margin-top: ' . $data['margin_top'];
            if (!empty($data['margin_right'])) $styles[] = 'margin-right: ' . $data['margin_right'];
            if (!empty($data['margin_bottom'])) $styles[] = 'margin-bottom: ' . $data['margin_bottom'];
            if (!empty($data['margin_left'])) $styles[] = 'margin-left: ' . $data['margin_left'];
        }
    }
    
    // Dimensions
    if (!empty($data['width'])) $styles[] = 'width: ' . $data['width'];
    if (!empty($data['height'])) $styles[] = 'height: ' . $data['height'];
    
    // Min/Max dimensions
    if (isset($data['use_min_width']) && $data['use_min_width'] && !empty($data['min_width'])) {
        $styles[] = 'min-width: ' . $data['min_width'];
    }
    if (isset($data['use_max_width']) && $data['use_max_width'] && !empty($data['max_width'])) {
        $styles[] = 'max-width: ' . $data['max_width'];
    }
    if (isset($data['use_min_height']) && $data['use_min_height'] && !empty($data['min_height'])) {
        $styles[] = 'min-height: ' . $data['min_height'];
    }
    if (isset($data['use_max_height']) && $data['use_max_height'] && !empty($data['max_height'])) {
        $styles[] = 'max-height: ' . $data['max_height'];
    }
    
    return !empty($styles) ? implode('; ', $styles) : '';
}

/**
 * Generate PHP code for a single block
